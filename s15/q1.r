# Create a vector
my_vector <- c(1, 2, 3)

# Create a matrix
my_matrix <- matrix(1:6, nrow = 2, ncol = 3)

# Create another list
my_sublist <- list(name = "Alice", age = 25)

# Combine all into a list
my_list <- list(Vector = my_vector, Matrix = my_matrix, SubList = my_sublist)

# Print the list
print("Full List with Named Elements:")
print(my_list)
