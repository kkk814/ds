# Sample vector
vec <- c(12, 5, 25, 8, 3, 19)

# Print original vector
cat("Original Vector:\n")
print(vec)

# Sort in ascending order
asc_vec <- sort(vec)
cat("\nVector in Ascending Order:\n")
print(asc_vec)

# Sort in descending order
desc_vec <- sort(vec, decreasing = TRUE)
cat("\nVector in Descending Order:\n")
print(desc_vec)
